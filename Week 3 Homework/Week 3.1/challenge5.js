function isLeapYear(year) {
    if (year % 400 === 0){
        return "Leap year";
    } else if (year % 100 === 0){
        return "Not a Leap year";
    } else if (year % 4 === 0){
        return "Leap year";
    } else { 
        return "Not a leap year"
    }
}
console.log(isLeapYear(2000)); 

