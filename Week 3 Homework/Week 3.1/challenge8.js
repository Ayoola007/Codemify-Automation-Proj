function checkTemp (temperature){
    if (temperature < 32){
        return "Freezing";
    } else if (temperature >= 32 && temperature <= 50){
        return "Cold";
    } else if (temperature >= 51 && temperature <= 70){
        return "Mild";
    } else if (temperature >= 71 && temperature <= 90){
        return "Warm";
    } else if (temperature > 90){
        return "Hot";
    }
}
console.log(checkTemp(30))

