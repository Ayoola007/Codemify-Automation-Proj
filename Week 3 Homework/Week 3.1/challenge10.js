function suggestMeal(time, diet){
    if (time === "morning") {
        if (diet === "vegan") {
            return "Vegan breakfast";
        } else if (diet === "vegetarian"){
            return "Vegetarian salad breakkie";
        } else if (diet === "omnivore") {
            return "chicken and beef breakie"
        }
    } else if (time === "afternoon"){
        if (diet === "vegan"){
            return "Vegan lunch";
        } else if (diet === "vegetarian"){
            return "Vegie lunch";
        } else if (diet === "omnivore"){
            return "lamb chops";
        }
    } else if (time === "evening") {
        if (diet === "vegan") {
            return "vegan dinner";
        } else if (diet === "vegetarian") {
            return "salad and fruits dinner";
        } else if (diet === "omnivore") {
            return "port and goat meat pepper soup"
        }
    }
}
console.log(suggestMeal("morning", "vegan"));

