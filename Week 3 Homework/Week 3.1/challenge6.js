function checkSeason(month){
    if (month === 12 || month === 1 || month === 2){
        return "winter";
    }else if (month >= 3 && month <= 5){
        return "Spring";
    } else if (month >= 6 && month <= 8){
        return "Summer"; 
    } else if (month >= 9 && month <= 11){
        return "Autumn"
    }
}
console.log(checkSeason(1));

